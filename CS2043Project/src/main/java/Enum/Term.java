package Enum;

/**
 * Defines the frequency terms for expenses.
 * This enum can be used to specify how often an expense occurs.
 */
public enum Term {
    /**
     * Represents an expense that occurs every week.
     */
    WEEKLY,

    /**
     * Represents an expense that occurs every two weeks.
     */
    BIWEEKLY,

    /**
     * Represents an expense that occurs once a month.
     */
    MONTHLY,

    /**
     * Represents an expense that occurs once a year.
     */
    YEARLY
}
